function stripHtml(html) {
    const temp = document.createElement("div");
    temp.innerHTML = html;
    return (temp.textContent || temp.innerText || "").replace(/\s+/g, " ").trim();
}

function projectPreview(projectId, maxLength = 150) {
    const text = stripHtml(translations[currentLang][projectId] || "");
    if (text.length <= maxLength) return text;
    return `${text.slice(0, maxLength).trimEnd()}...`;
}

function bindScrollLinks(scope = document) {
    scope.querySelectorAll('a[href^="#"]').forEach((anchor) => {
        anchor.onclick = (event) => {
            const href = anchor.getAttribute("href");
            if (!href) return;
            const targetId = href.slice(1) || "hero";
            event.preventDefault();
            scrollToSection(targetId);
            closeMobileMenu();
        };
    });
}

