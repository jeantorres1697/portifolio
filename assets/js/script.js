function initRevealObserver() {
    if (revealObserver) revealObserver.disconnect();

    revealObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add("show");
            }
        });
    }, { threshold: 0.14 });

    document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));
}

function initSectionObserver() {
    if (sectionObserver) sectionObserver.disconnect();

    const sections = document.querySelectorAll("main section[id]");
    const navItems = document.querySelectorAll(".nav-links a[data-section]");
    if (!sections.length || !navItems.length) return;

    sectionObserver = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (!entry.isIntersecting) return;

            navItems.forEach((item) => {
                item.classList.toggle("active", item.dataset.section === entry.target.id);
            });
        });
    }, { threshold: 0.45 });

    sections.forEach((section) => sectionObserver.observe(section));
}

function initHeaderState() {
    const header = document.getElementById("header");
    if (!header) return;

    const onScroll = () => {
        header.classList.toggle("is-scrolled", window.scrollY > 24);
    };

    onScroll();
    window.addEventListener("scroll", onScroll, { passive: true });
}

function scrollToSection(id) {
    const element = document.getElementById(id);
    if (!element) return;

    const headerOffset = 110;
    const elementPosition = element.getBoundingClientRect().top + window.pageYOffset;
    window.scrollTo({
        top: elementPosition - headerOffset,
        behavior: "smooth"
    });
}

function scrollToTop() {
    window.scrollTo({
        top: 0,
        behavior: "smooth"
    });
}

function updateBackToTop() {
    const button = document.getElementById("backToTop");
    if (!button) return;

    button.style.display = window.scrollY > 360 && !document.body.classList.contains("modal-open")
        ? "flex"
        : "none";
}

function scrollGallery(direction) {
    const gallery = document.getElementById("modalGallery");
    if (!gallery) return;

    gallery.scrollBy({
        left: direction * gallery.clientWidth,
        behavior: "smooth"
    });

    window.setTimeout(updateArrows, 320);
}

