function initLanguageSwitch() {
    const toggleLanguage = () => {
        currentLang = currentLang === "pt" ? "en" : "pt";

        [document.getElementById("lang-switch"), document.getElementById("lang-switch-mobile")].forEach((button) => {
            if (button) button.textContent = currentLang === "pt" ? "EN" : "PT";
        });

        render();
    };

    const desktopButton = document.getElementById("lang-switch");
    const mobileButton = document.getElementById("lang-switch-mobile");
    if (desktopButton) desktopButton.onclick = toggleLanguage;
    if (mobileButton) mobileButton.onclick = toggleLanguage;
}

function initForm() {
    const form = document.getElementById("contactForm");
    if (!form) return;

    const submitButton = form.querySelector('button[type="submit"]');
    if (!submitButton) return;

    form.addEventListener("submit", async (event) => {
        event.preventDefault();

        const formData = new FormData(form);
        formData.append("access_key", "063bdd8a-dba2-4f5f-bb59-5af2544b4eb9");

        const originalText = submitButton.textContent;
        submitButton.textContent = translations[currentLang]["sending"];
        submitButton.disabled = true;

        try {
            const response = await fetch("https://api.web3forms.com/submit", {
                method: "POST",
                body: formData
            });

            const data = await response.json();

            if (response.ok) {
                alert(translations[currentLang]["message-sent"]);
                form.reset();
            } else {
                alert(`${translations[currentLang]["message-error"]}${data.message}`);
            }
        } catch (error) {
            alert(translations[currentLang]["message-retry"]);
        } finally {
            submitButton.textContent = originalText;
            submitButton.disabled = false;
        }
    });
}

async function initVisitorCounter() {
    const footerContainer = document.querySelector(".footer-shell");
    if (!footerContainer) return;

    const existingCounter = footerContainer.querySelector(".visitor-counter");
    if (existingCounter) existingCounter.remove();

    const namespace = "jeantorres-portfolio-2025";
    const key = "visitas";

    try {
        const response = await fetch(`https://api.countapi.xyz/hit/${namespace}/${key}`);
        const data = await response.json();

        const counterDiv = document.createElement("div");
        counterDiv.className = "visitor-counter";
        counterDiv.style.marginTop = "16px";
        counterDiv.innerHTML = `
            <img src="https://img.shields.io/badge/Acessos-${data.value}-0078D7?style=flat-square&logo=google-analytics&logoColor=white"
                 alt="Contador de acessos"
                 style="display:block; max-width:100%;">
        `;

        footerContainer.appendChild(counterDiv);
    } catch (error) {
        console.log("Erro ao carregar contador.");
    }
}

function initGlobalEvents() {
    const closeButton = document.getElementById("closeModal");
    const modal = document.getElementById("projectModal");
    const zoomOverlay = document.getElementById("zoomOverlay");

    if (closeButton) closeButton.onclick = closeProjectModal;

    window.addEventListener("click", (event) => {
        if (event.target === modal) closeProjectModal();
    });

    window.addEventListener("keydown", (event) => {
        if (event.key === "Escape") {
            closeProjectModal();
            closeMobileMenu();
            if (zoomOverlay) zoomOverlay.style.display = "none";
        }
    });

    if (zoomOverlay) {
        zoomOverlay.onclick = () => {
            zoomOverlay.style.display = "none";
        };
    }

    window.addEventListener("scroll", updateBackToTop, { passive: true });
}

